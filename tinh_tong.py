# Chương trình tính tổng hai số nhập từ bàn phím
print("--- CHƯƠNG TRÌNH TÍNH TỔNG CỦA YẾN VY ---")

# Nhập dữ liệu
so_thu_nhat = float(input("Nhập số thứ nhất: "))
so_thu_hai = float(input("Nhập số thứ hai: "))

# Tính toán
tong = so_thu_nhat + so_thu_hai

# Xuất kết quả
print(f"Kết quả: {so_thu_nhat} + {so_thu_hai} = {tong}")